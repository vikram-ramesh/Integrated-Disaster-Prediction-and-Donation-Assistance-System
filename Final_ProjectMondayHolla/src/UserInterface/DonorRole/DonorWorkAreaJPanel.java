    private void donateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_donateBtnActionPerformed
        // TODO add your handling code here:
        
        int selectedRow = myTable.getSelectedRow();
        
        if(selectedRow <0)
        {
            JOptionPane.showMessageDialog(null, "Select a disaster event to donate");
            return;
        }
        
        WorkRequest request = (WorkRequest) myTable.getValueAt(selectedRow, 0);
        
        EmergencyJPanel emergencyJPanel = new EmergencyJPanel(userProcessContainer, account, enterprise, request);
        userProcessContainer.add("emergencyJPanel", emergencyJPanel);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
        
        
        
    }//GEN-LAST:event_donateBtnActionPerformed


